"use client";

import { useState } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { X, MessageCircle, Send } from 'lucide-react';
import Image from 'next/image';
import posthog from 'posthog-js';
import { useAppSelector } from '@/store/hooks';

export function WhatsAppButton() {
    const [isOpen, setIsOpen] = useState(false);
    const { isOpen: isCartOpen } = useAppSelector((state) => state.cart);
    const whatsappUrl = "https://wa.me/233549726818";

    if (isCartOpen) return null;

    return (
        <div className="whatsapp-button fixed bottom-6 sm:bottom-6 right-6 z-50 flex flex-col items-end pb-[env(safe-area-inset-bottom,0px)]">
            <AnimatePresence>
                {isOpen && (
                    <motion.div
                        initial={{ opacity: 0, y: 20, scale: 0.95 }}
                        animate={{ opacity: 1, y: 0, scale: 1 }}
                        exit={{ opacity: 0, y: 20, scale: 0.95 }}
                        className="mb-4 w-72 sm:w-80 bg-white dark:bg-neutral-900 rounded-2xl shadow-2xl border border-neutral-100 dark:border-neutral-800 overflow-hidden"
                    >
                        {/* Header */}
                        <div className="bg-black dark:bg-white p-4 flex items-center justify-between">
                            <div className="flex items-center gap-3">
                                <div className="relative w-10 h-10 rounded-full bg-black overflow-hidden border border-neutral-200 dark:border-neutral-700">
                                    <Image
                                        src="/logos/icon.png"
                                        alt="AWURABA Logo"
                                        fill
                                        className="object-contain p-1.5"
                                    />
                                </div>
                                <div>
                                    <p className="text-white dark:text-black text-sm font-bold">AWURABA</p>
                                    <div className="flex items-center gap-1.5">
                                        <div className="w-2 h-2 rounded-full bg-green-500 animate-pulse"></div>
                                        <p className="text-neutral-400 dark:text-neutral-500 text-[10px] uppercase tracking-widest font-bold">Online</p>
                                    </div>
                                </div>
                            </div>
                            <button
                                onClick={() => setIsOpen(false)}
                                className="text-neutral-400 hover:text-white dark:hover:text-black transition-colors"
                            >
                                <X className="w-5 h-5" />
                            </button>
                        </div>

                        {/* Body */}
                        <div className="p-6 bg-neutral-50 dark:bg-neutral-900/50">
                            <div className="bg-white dark:bg-black p-4 rounded-xl rounded-tl-none shadow-sm border border-neutral-100 dark:border-neutral-800 max-w-[90%]">
                                <p className="text-xs text-neutral-500 mb-1 font-bold uppercase tracking-tighter">Awuraba</p>
                                <p className="text-sm leading-relaxed">
                                    Welcome to AWURABA!
                                    <br />
                                    Do you have any questions about our collections or need assistance with your order? We're happy to help!
                                </p>
                                <p className="text-[10px] text-neutral-400 mt-2 text-right">
                                    {new Date().toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' })}
                                </p>
                            </div>
                        </div>

                        {/* Footer / CTA */}
                        <div className="p-4 bg-white dark:bg-neutral-900 border-t border-neutral-100 dark:border-neutral-800">
                            <a
                                href={whatsappUrl}
                                target="_blank"
                                rel="noopener noreferrer"
                                className="flex items-center justify-center gap-2 w-full bg-[#25D366] text-white py-3 rounded-xl font-bold text-sm transition-transform hover:scale-[1.02] active:scale-[0.98]"
                                onClick={() => {
                                    // PostHog: Track WhatsApp chat started
                                    posthog.capture('whatsapp_chat_started', {
                                        source: 'floating_button',
                                        current_page: typeof window !== 'undefined' ? window.location.pathname : null,
                                    });
                                    setIsOpen(false);
                                }}
                            >
                                <MessageCircle className="w-5 h-5" />
                                Start WhatsApp Chat
                            </a>
                            <p className="text-[10px] text-center text-neutral-400 uppercase tracking-widest mt-3">
                                Typically replies within minutes
                            </p>
                        </div>
                    </motion.div>
                )}
            </AnimatePresence>

            {/* Float Button */}
            <button
                onClick={() => setIsOpen(!isOpen)}
                className={`relative flex h-14 w-14 items-center justify-center rounded-full shadow-2xl transition-all duration-300 ${isOpen
                    ? 'bg-neutral-200 dark:bg-neutral-800 rotate-90 scale-90'
                    : 'bg-black text-white dark:bg-white dark:text-black hover:scale-110 animate-bounce-slow'
                    }`}
                aria-label="Chat on WhatsApp"
            >
                {!isOpen && (
                    <span className="absolute top-0 right-0 flex h-3 w-3">
                        <span className="animate-ping absolute inline-flex h-full w-full rounded-full bg-green-400 opacity-75"></span>
                        <span className="relative inline-flex rounded-full h-3 w-3 bg-green-500"></span>
                    </span>
                )}
                {isOpen ? (
                    <X className="h-6 w-6 text-black dark:text-white" />
                ) : (
                    <svg
                        viewBox="0 0 24 24"
                        fill="currentColor"
                        className="h-8 w-8"
                        xmlns="http://www.w3.org/2000/svg"
                    >
                        <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
                    </svg>
                )}
            </button>
        </div>
    );
}
